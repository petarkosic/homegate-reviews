				<p>
					<strong>Intent</strong>: The sole purpose of this app is to showcase
					my technical skills as a developer and does not intend to deceive or
					misuse data in any way.
				</p>
			</div>
			<div class="modal-buttons">
				<button @click="prevStep" v-if="step > 1">Previous</button>
				<button @click="nextStep" v-if="step < 3">Next</button>
				<button @click="closeModal">Close</button>
			</div>
		</div>
	</div>
</template>

